// Helper function to get icons for menu items
const getMenuItemIcon = (name: string) => {
  switch (name) {
    case 'Home':
      return <HomeIcon className="w-6 h-6 text-[#2d711c]" />;
    case 'Store':
      return <StoreIcon className="w-6 h-6 text-[#2d711c]" />;
    case 'Search':
      return <SearchIcon className="w-6 h-6 text-[#2d711c]" />;
    case 'Account':
      return <AccountIcon className="w-6 h-6 text-[#2d711c]" />;
    case 'Cart':
      return <CartIcon className="w-6 h-6 text-[#2d711c]" />;
    case 'My Orders':
      return <OrdersIcon className="w-6 h-6 text-[#2d711c]" />;
    case 'My Wishlist':
      return <HeartIcon className="w-6 h-6 text-[#2d711c]" />;
    case 'Contact Us':
      return <ContactIcon className="w-6 h-6 text-[#2d711c]" />;
    default:
      return null;
  }
};

